// Импортируем Markup из библиотеки Telegraf. 
// Это специальный инструмент для создания клавиатур.
const { Markup } = require('telegraf');

/**
 * Функция для создания главного меню.
 * Мы пишем её отдельно, чтобы не захламлять основной код.
 */
function getMainMenu() {
    return Markup.keyboard([
        ['Сколько время? ⏰', 'Как тебя зовут? 🤔'], // Первый ряд кнопок
        ['Подбодри меня! 🌟', 'Связь с создателем 👨‍💻'] // Второй ряд кнопок
    ])
    .resize(); // Эта команда делает кнопки аккуратными (по размеру текста)
}

// // ПОДСКАЗКА ДЛЯ НОВИЧКА: // //
// Чтобы эта функция была доступна в других файлах (например, в index.js),
// мы должны её "экспортировать". Это как выставить товар на витрину.
module.exports = { getMainMenu };

